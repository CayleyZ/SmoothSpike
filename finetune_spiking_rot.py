class SimpleMetric:
    def __init__(self, is_regression=False):
        self.is_regression = is_regression
        self.preds = []
        self.refs = []

    def add_batch(self, predictions, references):
        self.preds.extend(predictions.detach().cpu().view(-1).tolist())
        self.refs.extend(references.detach().cpu().view(-1).tolist())

    def compute(self):
        if not self.refs:
            return {"mse" if self.is_regression else "accuracy": 0.0}
        if self.is_regression:
            mse = sum((float(p) - float(r)) ** 2 for p, r in zip(self.preds, self.refs)) / len(self.refs)
            return {"mse": mse}
        correct = sum(int(int(p) == int(r)) for p, r in zip(self.preds, self.refs))
        return {"accuracy": correct / len(self.refs)}
